    public void loadHoKhauTable(){
        DefaultTableModel tableModel = (DefaultTableModel)ho_khau_table.getModel();
        int rowCount = tableModel.getRowCount();
        for (int i = rowCount; i > 0 ; i--){
            tableModel.removeRow(i-1);
        }
        List<HoKhau> hoKhauList = HoKhauDAO.getListHoKhau();
        for(HoKhau hoKhau : hoKhauList){
            tableModel.addRow(new Object[]{hoKhau.getMaHo(),hoKhau.getTenChuHo(),hoKhau.getSoThanhVien(),
                            hoKhau.getDienTich(),hoKhau.getSoXe(),hoKhau.getSoOto()});
        }
    }

    public void loadNhanKhauTable(){
        DefaultTableModel tableModel = (DefaultTableModel)nhan_khau_table.getModel();
        int rowCount = tableModel.getRowCount();
        for (int i = rowCount; i > 0 ; i--){
            tableModel.removeRow(i-1);
        }
        List<NhanKhau> nhanKhauList = NhanKhauDAO.getListNhanKhau();
        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
        for(NhanKhau nhanKhau : nhanKhauList){
            tableModel.addRow(new Object[]{nhanKhau.getId(),nhanKhau.getHoTen(),nhanKhau.getGioiTinh(),
                    nhanKhau.getCccd(),formatter.format(nhanKhau.getNgaySinh()),nhanKhau.getQuocTich(),
                    nhanKhau.getSoDienThoai(),nhanKhau.getMaHo(),nhanKhau.getQuanHeChuHo()});
        }
    }

